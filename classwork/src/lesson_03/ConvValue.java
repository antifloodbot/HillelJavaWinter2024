package lesson_03;

public class ConvValue {
    public static void main(String[] args) {
        float fValue = 33.22f;
        int iValue = (int) fValue;

        System.out.println(fValue);
        System.out.println(iValue);


        short sValue = 34;
        iValue = sValue;

        System.out.println(sValue);
        System.out.println(iValue);
    }
}
