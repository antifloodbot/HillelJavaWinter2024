package lesson_03;

public class Str {
    public static void main(String[] args) {
        System.out.println(2 + 2);
        System.out.println(2 + "2");
        System.out.println(2 + '2');
        System.out.println(2 + 111);
        System.out.println("2" + 2);
        System.out.println("" + 2 + 2);
    }
}
